package day04_Variables;

public class Variables_Practice {

    public static void main(String[] args) {

        /*declare the following variables:
        num1 = 100;
        num2 = 10000;
        num3 = 3.5;
        num4 = 2.5f;
        num5 = 999999999999;
        num6 = 850000;
        print each of the variables above on the console */
        byte num1 = 100;
        short num2 = 10000;
        double num3 = 3.5;
        float num4 = 2.5f;
        long num5 = 999999999999L;
        int num6 = 850000;

        System.out.println("Byte " + num1);
        System.out.println("Short " + num2);
        System.out.println("Double " + num3);
        System.out.println("Float " + num4);
        System.out.println("Long " + num5);
        System.out.println("Int " + num6);

    }

}
